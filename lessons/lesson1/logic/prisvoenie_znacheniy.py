a = 8
b = 4

print('Assign Values:\t\t', 'a=', a, '\tb=', b)

a += b
print('Присвоение нового значения первой переменной ', 'a=', a, '(8 += 4)')

a -= b
print('Присвоение нового значения первой переменной ', 'a=', a, '(8 -= 4)')

a *= b
print('Присвоение нового значения первой переменной ', 'a=', a, '(8 *= 4)')

a /= b
print('Присвоение нового значения первой переменной ', 'a=', a, '(8 /= 4)')

a %= b
print('Присвоение нового значения первой переменной ', 'a=', a, '(8 %= 4)')
